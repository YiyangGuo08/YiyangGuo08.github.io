function confirmCD1() {
    // 显示确认对话框
    var result = confirm("Are you sure you want this item?");
    
    if (result) {
        // window.location.href = "https://space.bilibili.com/4523722/favlist";
        window.open("CD/CD1.html", '_blank');   // 重新打开一个窗口
        removeImage("CD1");
    } else {
        // 如果用户点击了"取消"按钮，则取消跳转
        return false;
    }
}

